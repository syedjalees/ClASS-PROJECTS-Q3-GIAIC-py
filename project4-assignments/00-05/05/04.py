def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Start at 10, stop before 0, step by -1
        print(i, end=" ")  # Print each number on the same line separated by spaces
    
    print("\nLiftoff!")  # After the loop finishes, print "Liftoff!"

# Ensure the script runs correctly
if __name__ == '__main__':
    main()
