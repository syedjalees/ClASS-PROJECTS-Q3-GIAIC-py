def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Starting from 10, ending before 0, step -1
        print(i, end=" ")  # Print each number followed by a space
    
    # Print Liftoff! after the countdown
    print("Liftoff!")

# Ensure the script runs correctly
if __name__ == '__main__':
    main()
